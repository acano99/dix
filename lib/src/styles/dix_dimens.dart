/// Clase con dimensiones comunmente usadas en el desarrollo
/// de aplicaciones.
class DixDimens {
  /// Dimensión pequeña, típicamente usada para espaciados o tamaños mínimos.
  static const double small = 8.0;

  /// Dimensión normal, un tamaño estándar para muchos elementos de UI.
  static const double normal = 12.0;

  /// Dimensión media, ligeramente más grande que la normal.
  static const double medium = 16.0;

  /// Dimensión alta, para elementos que requieren más espacio.
  static const double hig = 20.0;

  /// Dimensión grande, para elementos prominentes o espaciados generosos.
  static const double large = 24.0;

  /// Dimensión muy grande, para elementos que necesitan destacar mucho.
  static const double veryLarge = 28.0;
}
